function parseIntoHTML(elements, fontSize, addClass) {
    var numOfElements = elements.length;
    var z = "";
    for(var i=0; i<numOfElements; i++) {
        z += ("<" + fontSize + " class = " + "\"" + addClass + "\">" + elements[i] + "</" + fontSize + ">");
    }
    return z;
}


